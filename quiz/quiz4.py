"""
# 학생 성적 프로그램

- 학생별로 수강하는 과목수도 다르고 과목명도 다든 경우를 가정함.

- 딕셔너리, 리스트를 사용해 학생마다 수강하는 과목이 다른 경우를 처리할 수 있는 자료 구조를 구성하고 동일하게 이름을 검색해 성적을 출력하는 코드를 작성.
- 최대한 간결한 코드로 작성.

# 실행 결과 - 예시
이름을 입력하세요: 김철수  
성적표: 김철수 
==============================
웹프    파이썬  C++     영어회화
------------------------------
85      90      88      80
==============================
총점: 343, 평균: 85.75

이름을 입력하세요: 홍길동
성적표: 홍길동 
==============================
자바    파이썬  컴공개론
------------------------------
90      87      95
==============================
총점: 272, 평균: 90.67
"""


data = {
    "김철수": {"웹프": 85, "파이썬": 90, "C++": 88, "영어회화": 80},
    "홍길동": {"자바": 90, "파이썬": 87, "컴공개론": 95}
}

name = input("이름을 입력하세요: ")

if name in data:
    scores = data[name]
    subjects = scores.keys()
    values = scores.values()
    
    # 성적표 출력
    print(f"성적표: {name}")
    print("=" * 30)
    print("\t".join(subjects))
    print("-" * 30)
    print("\t".join(map(str, values)))
    print("=" * 30)
    
    # 계산 및 출력
    total = sum(values)
    avg = total / len(values)
    print(f"총점: {total}, 평균: {avg:.2f}")
else:
    print("해당 학생을 찾을 수 없습니다.")


